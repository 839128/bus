    private static int LAST_YEAR = Integer.parseInt(YEAR_OF_DAY[YEAR_OF_DAY.length - 1].substring(0, 4));

    /**
     * 公历日期转农历日期,公历日期合法性经过检查. 推荐的调用方法
     * 支持格式 ：20240527｜2024-05-27｜2024/05/27｜2024.05.27｜2024年05月27日
     *
     * @param date 公历日期
     * @return 农历日期
     */
    public static String of(String date) {
        int[] dates = judge(date);
        if (judge(dates)) {
            if (dates[0] < FIRST_YEAR || dates[0] > LAST_YEAR) {
                throw new RuntimeException("-输入的日期年份超出范围,年份必须在" + FIRST_YEAR + "与" + LAST_YEAR + "之间-");
            } else {
                String text = addZero(dates[0], 4) + addZero(dates[1], 2) + addZero(dates[2], 2);
                int[] result = cast(text);
                StringBuilder sb = new StringBuilder();
                sb.append(formatYear(result[0]));
                sb.append("年");
                sb.append(formatMonth(result[1]));
                sb.append("月");
                sb.append(formatDay(result[2]));
                return sb.toString();
            }
        }
        throw new InternalException("-输入的日期不合法-");
    }

